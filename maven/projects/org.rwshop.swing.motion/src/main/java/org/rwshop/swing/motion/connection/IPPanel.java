    private boolean validateIP(String address) {
        String[] dotQuad = address.trim().split("\\.");
        
        if(dotQuad.length != 4) { // IP is four segments separated by .s
            return false;
        }
        
        for(String segment: dotQuad) {
            try {
                Integer segInt = Integer.parseInt(segment);
                
                if(segInt < 0 || segInt > 255) {
                    // each segment is 0-255
                    return false;
                }
            } catch(NumberFormatException e) {
                // each segment must be a valid int
                return false;
            }
        }
        
        return true;
    }
    
    @Override
    public UniqueService getValue() {
        String ipAddress = myIPAddress;
        String serial = UUID.randomUUID().toString();
        boolean physical = jRadioButton1.isSelected();
        Map<String, String> props = new HashMap<String, String>();
        
        myIPAddress = null;
        props.put("robotId", physical ? "myRobot" : "Avatar_ZenoR50");
        
        return ipAddress == null ?
                null : new UniqueService(ipAddress, serial, props);
    }
    
    void cancel() {
        SelectorFrame frame = SelectorFrame.getInstance();
        frame.setVisible(false);
        myIPAddress = "CANCEL";
    }
    
