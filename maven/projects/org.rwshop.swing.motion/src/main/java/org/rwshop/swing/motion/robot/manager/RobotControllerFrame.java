    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if(myController == null){
            return;
        }
        ServoRobotConfig conf = new ServoRobotConfig();
        conf.setRobotId(new Robot.Id("I am a robot not a number"));
        Robot r = new ServoRobot(conf);
        myRegistration = RobotUtils.registerRobot(myContext, r, null);
        myController.setRobot(r);
    }//GEN-LAST:event_jButton1ActionPerformed

