    @Override
    public void propertyChange(PropertyChangeEvent evt) {
        if(evt == null){
            return;
        }else if(RobotController.PROP_ROBOT.equals(evt.getPropertyName())){
            repopulatePanel();
        //}else if(RobotController.PROP_SELECT_ROBOT.equals(evt.getPropertyName())){
        //    populateSelected();
        }else if(Robot.PROP_CONNECTED.equals(evt.getPropertyName())){
            populateConnection();
        }else if(Robot.PROP_ENABLED.equals(evt.getPropertyName())){
            populateEnabled();
        }
    }
