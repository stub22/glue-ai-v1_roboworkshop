    public void setRobotController(RobotController robotController){
        if(myRobotController != null){
            myRobotController.removePropertyChangeListener(this);
        }
        myRobotController = robotController;
        if(myRobotController != null){
            myRobotController.addPropertyChangeListener(this);
        }
        repopulatePanel();
    }
    
    private Robot getRobot(){
        if(myRobotController == null){
            return null;
        }
        return myRobotController.getRobot();
    }
    
    private void repopulatePanel(){
        populateInfo();
        populateConnection();
        populateEnabled();
        populateSelected();
        populateDefPos();
    }
    
    private void populateInfo(){
        Robot r = getRobot();
        String id = r == null ? "--" : r.getRobotId().getRobtIdString();
        lblRobotId.setText(id);
        String type = r == null ? "--" : r.getClass().getSimpleName();
        lblRobotType.setText(type);
    }
    
    private void populateConnection(){
        Robot r = getRobot();
        if(r == null){
            lblConnection.setText("--");
            btnConnect.setEnabled(false);
            btnDisconnect.setEnabled(false);
        }else if(r.isConnected()){
            lblConnection.setText("Connected");
            btnConnect.setEnabled(false);
            btnDisconnect.setEnabled(true);
        }else{
            lblConnection.setText("Disconnected");
            btnConnect.setEnabled(true);
            btnDisconnect.setEnabled(false);
        }
        populateEnabled();
    }
    
    private void populateEnabled(){
        Robot r = getRobot();
        if(r == null){
            lblEnabled.setText("--");
            btnEnable.setEnabled(false);
            btnDisable.setEnabled(false);
        }else if(!r.isConnected()){
            lblEnabled.setText("Disabled");
            btnEnable.setEnabled(false);
            btnDisable.setEnabled(false);
        }else if(r.isEnabled()){
            lblEnabled.setText("Enabled");
            btnEnable.setEnabled(false);
            btnDisable.setEnabled(true);
        }else{
            lblEnabled.setText("Disabled");
            btnEnable.setEnabled(true);
            btnDisable.setEnabled(false);
        }
        populateDefPos();
    }
    
    private void populateSelected(){
        //if(getRobot() == null){
            lblSelected.setText("--");
            btnSelect.setEnabled(false);
        //}else{
        //    lblSelected.setText("Select");
        //    btnSelect.setEnabled(true);
        //}
    }
    
    private void populateDefPos(){
        Robot r = getRobot();
        if(r == null){
            btnSetDefaultPos.setEnabled(false);
        }else if(!r.isConnected() || !r.isEnabled()){
            btnSetDefaultPos.setEnabled(false);
        }else{
            btnSetDefaultPos.setEnabled(true);
        }
    }
    

